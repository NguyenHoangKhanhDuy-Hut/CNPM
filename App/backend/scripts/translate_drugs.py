# Replacement patterns: sort by length descending for longest match first
DRUG_NAME_PATTERNS = sorted(DRUG_NAME_TRANSLATIONS.items(), key=lambda x: -len(x[0]))


def translate_drug_name(name: str) -> str:
    for eng, vie in DRUG_NAME_PATTERNS:
        pattern = re.compile(re.escape(eng), re.IGNORECASE)
        name = pattern.sub(vie, name)
    # Also translate active ingredient names in parentheses
    def trans_ingredient(m):
        inner = m.group(1)
        translated = translate_text(inner)
        # Clean up excessive spaces
        translated = re.sub(r'\s+', ' ', translated).strip()
        return f"({translated})" if translated else f"({inner})"
    name = re.sub(r'\(([^)]+)\)', trans_ingredient, name)
    name = re.sub(r'\s+', ' ', name).strip()
    # Remove double spaces from empty article removal
    name = re.sub(r'\s+', ' ', name).strip()
    return name


