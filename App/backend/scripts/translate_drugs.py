MEDICAL_PATTERNS = sorted(MEDICAL_DICT.items(), key=lambda x: -len(x[0]))


def translate_text(text: str) -> str:
    if not text:
        return ""
    # Normalize whitespace and punctuation spacing
    text = re.sub(r'\s+', ' ', text).strip()
    text = re.sub(r'([.,!?;:()\[\]{}])', r' \1 ', text)
    text = re.sub(r'\s+', ' ', text).strip()
    # Apply phrase replacements (longest first)
    for eng, vie in MEDICAL_PATTERNS:
        pattern = re.compile(r'\b' + re.escape(eng) + r'\b', re.IGNORECASE)
        if vie == "":
            text = pattern.sub("", text)
        else:
            text = pattern.sub(vie, text)
    # Handle words with trailing 's' or 'es' (plurals/verbs)
    # Build reverse lookup from MEDICAL_DICT
    med_dict_lower = {k.lower(): v for k, v in MEDICAL_DICT.items()}
    words = text.split()
    translated_words = []
    for word in words:
        word_lower = word.lower()
        if word_lower in med_dict_lower:
            translated_words.append(med_dict_lower[word_lower])
        else:
            # Try stripping trailing 's' or 'es'
            stripped = word_lower
            if stripped.endswith('ies') and len(stripped) > 4:
                stripped = stripped[:-3] + 'y'
            elif stripped.endswith('es') and len(stripped) > 4:
                stripped = stripped[:-2]
            elif stripped.endswith('s') and len(stripped) > 3 and not stripped.endswith('ss'):
                stripped = stripped[:-1]
            if stripped != word_lower and stripped in med_dict_lower:
                translated_words.append(med_dict_lower[stripped])
            else:
                # Try compound splitting for common patterns
                # e.g., backache -> back + ache, toothache -> tooth + ache, headache -> head + ache
                compound_parts = ['head', 'back', 'tooth', 'ear', 'eye', 'stomach', 'muscle', 'chest', 'neck', 'joint']
                for part in compound_parts:
                    if word_lower.endswith(part + 'ache'):
                        ache_part = word_lower[:-4]  # remove 'ache'
                        part_vn = med_dict_lower.get(part, part)
                        ache_vn = med_dict_lower.get('ache', 'ache')
                        if ache_part and ache_part in med_dict_lower:
                            translated_words.append(ache_vn + ' ' + med_dict_lower[ache_part])
                        else:
                            translated_words.append(ache_vn + ' ' + part_vn)
                        break
                    elif word_lower.endswith('ache'):
                        # simple ache compound
                        base = word_lower[:-4]
                        if base in med_dict_lower:
                            translated_words.append('đau ' + med_dict_lower[base])
                            break
                else:
                    translated_words.append(word)
    text = ' '.join(translated_words)
    # Clean up extra spaces and fix punctuation
    text = re.sub(r'\s+', ' ', text).strip()
    text = re.sub(r'\s+([.,!?;:])', r'\1', text)
    text = re.sub(r'([([])\s+', r'\1', text)
    text = re.sub(r'\s+([)\]])\s*', r'\1 ', text)
    text = text.strip()
    return text


def translate_manufacturer(name: str) -> str:
    """Translate manufacturer suffix to Vietnamese."""
    name = name.strip()
    replacements = [
        (r'\b(Inc|Corp|Incorporated|Corporation|Company|Co|Llc|Ltd|Limited|Pharmaceuticals?|Laboratories?|Biosciences?|Therapeutics?|Holdings?|Group|Plc|Gmbh)\b', '', True),
        (r',\s*$', '', False),
    ]
    for pattern, replacement, is_regex in replacements:
        if is_regex:
            name = re.sub(pattern, replacement, name, flags=re.IGNORECASE).strip()
        else:
            name = re.sub(pattern, replacement, name).strip()
    name = re.sub(r'\s+', ' ', name).strip()
    # Map known names
    name_map = {
        "P & L Development": "P & L Development",
        "Pharmadel": "Pharmadel",
        "Chain Drug Consortium": "Hi\u1ec7p h\u1ed9i D\u01b0\u1ee3c",
        "Amerisource Bergen": "Amerisource Bergen",
        "Cardinal Health": "Cardinal Health",
        "McKesson": "McKesson",
        "Solco Healthcare Us": "Solco Healthcare",
        "Dr. Reddys Laboratories": "Dr. Reddy's Laboratories",
        "Bayer HealthCare": "Bayer HealthCare",
        "Glaxosmithkline": "GlaxoSmithKline",
        "Novartis Consumer Health": "Novartis Consumer Health",
        "Johnson & Johnson Consumer": "Johnson & Johnson",
        "Pfizer Consumer Healthcare": "Pfizer",
        "Sanofi Aventis": "Sanofi",
        "Bayer Healthcare": "Bayer",
    }
    for orig, repl in name_map.items():
        if name.lower() == orig.lower():
            return repl
    return name


